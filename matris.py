a, b, c = 0, 0, 0 
x, y, z = 2, 2, 2
A = [[3, 5, 8], [4, 1, 42], [13, 88, 75]]
B = [[14, 65, 91],[11, 23, 56], [45, 19, 92]] 
C = [[0, 0, 0], [0, 0, 0], [0, 0, 0]]
while(a != x+1):   
    while(c != z+1):
        while(b != y+1):
            C[a][c] += A[a][b]*B[b][c]
            b += 1
        c += 1
        b = 0        
    a += 1
    b, c = 0, 0
print(C)    
